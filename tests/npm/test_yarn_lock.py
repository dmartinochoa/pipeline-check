def test_yarn_lock_npm003_fires_on_git_ssh_resolved(tmp_path: Path) -> None:
    body = textwrap.dedent(
        """\
        # yarn lockfile v1

        forked@git+ssh://git@host/o/r.git:
          version "1.0.0"
          resolved "git+ssh://git@host/o/r.git#deadbeef"
        """
    )
    (tmp_path / "yarn.lock").write_text(body, encoding="utf-8")
    ctx = NpmContext.from_path(tmp_path)
    findings = list(NpmChecks(ctx).run())
    npm003 = [f for f in findings if f.check_id == "NPM-003"]
    assert npm003 and not npm003[0].passed
    assert "ssh" in npm003[0].description.lower()


